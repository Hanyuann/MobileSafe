package cn.edu.sdu.mobilesafe.bean;

public class Virus {
	public String md5;
	public String desc;

	public String getMd5() {
		return md5;
	}

	public void setMd5(String md5) {
		this.md5 = md5;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
